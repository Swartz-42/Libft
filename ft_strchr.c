#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*st;

	i = 0;
	st = (char *)s;
	while (st[i] != '\0')
	{
		if (st[i] == c)
			return (&st[i]);
		i++;
	}
	if (st[i] == c)
		return (&st[i]);
	return (0);
}
